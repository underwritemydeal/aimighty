// Get translation for a key
export function t(key: string, lang: LanguageCode = 'en'): string {
  return translations[lang]?.[key] || translations['en']?.[key] || key;
}

// Language instruction for Claude system prompt
export function getLanguageInstruction(langCode: LanguageCode): string {
  if (langCode === 'en') return '';

  const lang = getLanguage(langCode);
  return `The user has selected ${lang.name}. Respond entirely in ${lang.name}. Maintain the same voice, tone, personality, and wisdom. Cite scriptures and sacred texts in ${lang.name} where standard translations exist. If the user writes in ${lang.name}, always respond in ${lang.name}.\n\n`;
}
